const quickSlotPattern = /^Switch to session in quick slot (\d+)$/

export function translateCommandText(text: string | undefined, language: TuiLanguage): string | undefined {
  if (text === undefined || language !== "zh-CN") return text
  const mapped = commandTextZhCN[text]
  if (mapped) return mapped
  const quickSlot = quickSlotPattern.exec(text)
  if (quickSlot) return `切换到快捷位 ${quickSlot[1]} 的会话`
  return text
}

export function resolveTuiLanguage(value: TuiLanguageConfig | undefined): TuiLanguage {
  if (value === "en" || value === "zh-CN") return value
  const locale = Intl.DateTimeFormat().resolvedOptions().locale || process.env.LANG || ""
  return locale.toLowerCase().startsWith("zh") ? "zh-CN" : "en"
}

export function createTuiI18n(language: TuiLanguage) {
  const dict = language === "zh-CN" ? zhCN : en
  return {
    language,
    t(key: TuiMessageKey, values?: Record<string, string | number>) {
      let text = dict[key] ?? en[key]
      if (!values) return text
      for (const [name, value] of Object.entries(values)) {
        text = text.replaceAll(`{${name}}`, String(value))
      }
      return text
    },
  }
}

export function formatMcpConnectionError(
  error: string | undefined,
  i18n: Pick<ReturnType<typeof createTuiI18n>, "t">,
) {
  let reason = i18n.t("sidebar.mcp.error.unknown")
  if (error?.includes("SSE error") && error.includes("405")) {
    reason = i18n.t("sidebar.mcp.error.sse405")
  } else if (error?.includes("socket connection was closed unexpectedly")) {
    reason = i18n.t("sidebar.mcp.error.socketClosed")
  } else if (error) {
    reason = error
  }
  return i18n.t("sidebar.mcp.failed", { reason })
}
